apple = 15.5
orange = 20
grape = 10.25
total_volume = apple + orange + grape
print("Total volume of juice:", total_volume)
total_int = int(total_volume)
print("Total volume in integer:", total_int)
total_str = str(total_volume)
print("Total volume as string:", total_str)
import random
random_number = random.randrange(5,10)
final_total = total_volume + random_number
print("Final total after adding random number:", final_total)